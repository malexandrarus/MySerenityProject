package com.fasttrackit.model;

public class BillingInformation {
    private String billingFirstName;
    private String billingLastName;
    private String billingEmail;
    private String billingAddress;
    private String billingCity;
    private String billingCountryDropdown;
    private String billingRegionDropdown;
    private String billingPostCode;
    private String billingPhoneNumber;

    public String getBillingFirstName() {
        return billingFirstName;
    }

    public void setBillingFirstName(String billingFirstName) {
        this.billingFirstName = billingFirstName;
    }

    public String getBillingLastName() {
        return billingLastName;
    }

    public void setBillingLastName(String billingLastName) {
        this.billingLastName = billingLastName;
    }

    public String getBillingEmail() {
        return billingEmail;
    }

    public void setBillingEmail(String billingEmail) {
        this.billingEmail = billingEmail;
    }

    public String getBillingAddress() {
        return billingAddress;
    }

    public void setBillingAddress(String billingAddress) {
        this.billingAddress = billingAddress;
    }

    public String getBillingCity() {
        return billingCity;
    }

    public void setBillingCity(String billingCity) {
        this.billingCity = billingCity;
    }

    public String getBillingCountryDropdown() {
        return billingCountryDropdown;
    }

    public void setBillingCountryDropdown(String billingCountryDropdown) {
        this.billingCountryDropdown = billingCountryDropdown;
    }

    public String getBillingRegionDropdown() {
        return billingRegionDropdown;
    }

    public void setBillingRegionDropdown(String billingRegionDropdown) {
        this.billingRegionDropdown = billingRegionDropdown;
    }

    public String getBillingPostCode() {
        return billingPostCode;
    }

    public void setBillingPostCode(String billingPostCode) {
        this.billingPostCode = billingPostCode;
    }

    public String getBillingPhoneNumber() {
        return billingPhoneNumber;
    }

    public void setBillingPhoneNumber(String billingPhoneNumber) {
        this.billingPhoneNumber = billingPhoneNumber;
    }
}
